// Render icons
function renderIcons() {
    const grid = document.getElementById('iconGrid');
    
    if (filteredApps.length === 0) {
        grid.innerHTML = `
            <div class="no-results">
                <svg fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9.172 16.172a4 4 0 015.656 0M9 10h.01M15 10h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"></path>
                </svg>
                <p>No icons found</p>
            </div>
        `;
        return;
    }

    grid.innerHTML = filteredApps.map((app, index) => `
        <div class="icon-card">
            <div class="icon-image">
                <img src="${app.iconUrl}" alt="${app.name}" loading="lazy">
            </div>
            <div class="icon-name">${app.name}</div>
            <div class="icon-category">${app.category}</div>
            <div class="icon-source">${
                app.source === 'aaron-parker' || app.source === 'github' ? 'Aaron Parker' : 
                app.source === 'official' ? 'Official' :
                app.source === 'dashboard-icons' ? 'Dashboard Icons' :
                'Chocolatey'
            }</div>
            <div class="icon-actions">
                <button class="btn-download" onclick="downloadIcon(${index})">
                    Download
                </button>
                <button class="btn-preview" onclick="previewIcon(${index})">
                    Preview
                </button>
            </div>
        </div>
    `).join('');
}

// Filter icons
function filterIcons() {
    const search = document.getElementById('searchInput').value.toLowerCase();
    
    // Track search if user typed something meaningful (3+ characters)
    if (search && search.length >= 3 && window.goatcounter) {
        window.goatcounter.count({
            path: '/search?q=' + encodeURIComponent(search),
            title: 'Search: ' + search,
            event: true
        });
    }
    
    filteredApps = apps.filter(app => {
        const matchesSearch = !search || app.name.toLowerCase().includes(search) || app.slug.toLowerCase().includes(search);
        const matchesCategory = currentCategory === 'all' || app.category === currentCategory;
        return matchesSearch && matchesCategory;
    });

    renderIcons();
    updateStats();
}

// Filter by category
